public class PrintExample {

  public static void main (String [] args) {

    int a=10;
    float b = (float) 10.5;
    final double PI=3.1415926;

    double d = 45e21;

    String s = "Hello World\n";
    System.out.println("a = " + a + " b = " + b + " PI = " + PI + " d = " + d); 
    System.out.println("s = " + s);
  }
}
